    <section class="people-interview">
      <h3>大切なのは、<br>語学力よりも熱意と積極性</h3>
      <h4>——「105」の活動内容を教えてください。</h4>
      <p>留学生の生活を全般的にサポートしています。レポートの手伝いやサークルの紹介、日本人学生と交流できるイベントの開催など、学生生活の手助けが中心です。とはいえ、日本に来たばかりの留学生には、日常生活でもわからないことがたくさんあります。たとえば先日は、銀行口座が作れずに困っている留学生の代わりに、口座開設の問い合わせをしました。活動内容をはじめから限定せずに、些細な困りごとでもフォローする。それが「105」のスタンスです。</p>

      <h4>——スタッフとして参加しているのは、どんな学生たちですか？</h4>
      <p>意外に思われるかもしれませんが、英語が得意な学生ばかりではありません。共通しているのは熱意と行動力ですね。「留学生と友達になりたい！コミュニケーションしたい！」という思いの強い人が集まっています。メンバーを見ていると、カタコトの英語でも意欲さえあれば意思疎通はできると実感します。</p>

      <h4>——活動の頻度はどの程度でしょう？</h4>
      <p>部活やサークルと違って、「105」には決まった活動日がありません。サークルやアルバイトで忙しいメンバーもいますが、それぞれが自分のペースで「105」の活動時間を大切にしてくれています。参加者200人規模の大きなイベントを準備する時などは、主体的に動いてくれるメンバーに助けられていますね。</p>

      <h3>メンバー増加に伴い、<br>組織づくりを意識するように</h3>
      <h4>——運営メンバーは全部で何名ぐらいいるのでしょうか。</h4>
      <p>現在活動しているのは、大体40人くらいでしょうか。グローバルな意識を持った学生が年々増えてきていることもあり、メンバー希望者が増えているので、昨年から組織としての体制づくりを模索しています。告知を担当する広報部、資金管理をする会計部など、役割に応じた部局をいくつか作り、全体で顔を合わせずとも各自で動けるような形を整えました。最近も新たに4つの部局が新設されるなど、まだ試行錯誤の段階です。私は室長（代表）の立場なので、組織がスムーズに活動できるよう、全体をマネジメントする役割を担っています。</p>

      <h4>——責任の重いポジションだと、苦労も多いのでは？</h4>
      <p>11月に開催した合宿イベントの「アステージキャンプ」では、宿泊先の手配や当日の段取りなど重要事項がいくつかあり、何かあったら自分の責任というのはプレッシャーでした。でもイベント後に参加者から「ありがとう」と言葉をかけてもらえたことで、それまでの苦労が吹き飛びました。</p>

      <h3>メンバーを引っ張り<br>交流の企画を増やす</h3>
      <h4>——活動を通じて、自分自身が成長したと感じるのはどんなところですか？</h4>
      <p>チームを動かす力、でしょうか。活動を始めた当初は、率先して動こうとするあまり、自分の意見にこだわりすぎてしまうこともありました。しかし何度か失敗を経験するなかで、それでは組織がうまく回らないことを痛感します。ただメンバーを引っ張ろうとするのではなく、相互に意見を交わしながら物事を進めることが大事だと、この時に学びました。思い出したのは、当時受講していた「経営行動科学」や「組織論」での学びです。それまで座学にあまり興味が持てませんでしたが、この授業でのケーススタディは自分の状況とも重なっていて、多くのヒントが得られました。</p>

      <h4>——最後に、読者の方々へのメッセージをいただけますか？</h4>
      <p>横国の留学生に限らず、日本にくる外国人の方々みんなに共通して言えるのは、「日本に興味があるからここまでやって来ている」ということです。日本人と話したいという気持ちをみんな持っていますし、話しかけられて嫌な顔をする人はいません。国際交流をしてみたい人、語学力を伸ばしたい人はぜひ留学生に話しかけてみてください。フレンドリーな彼ら、彼女らに関わることで、そこからきっと何かが生まれるはずです。</p>

